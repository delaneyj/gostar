package  elements

type AElement struct {
    *Element
}

func A() *AElement {
    return &AElement{
        Element: &Element{
            Tag: "a",
            IsSelfClosing: false,
        },
    }
}

func (e *AElement) AddChildren(children ...*Element) *AElement {
    e.Children = append(e.Children, children...)
	return e
}


