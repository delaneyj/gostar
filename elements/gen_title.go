package  elements

type TitleElement struct {
    *Element
}

func TITLE() *TitleElement {
    return &TitleElement{
        Element: &Element{
            Tag: "title",
            IsSelfClosing: false,
        },
    }
}

func (e *TitleElement) AddChildren(children ...*Element) *TitleElement {
    e.Children = append(e.Children, children...)
	return e
}


