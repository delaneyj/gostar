package  elements

type OptgroupElement struct {
    *Element
}

func OPTGROUP() *OptgroupElement {
    return &OptgroupElement{
        Element: &Element{
            Tag: "optgroup",
            IsSelfClosing: false,
        },
    }
}

func (e *OptgroupElement) AddChildren(children ...*Element) *OptgroupElement {
    e.Children = append(e.Children, children...)
	return e
}


