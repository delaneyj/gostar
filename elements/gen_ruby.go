package  elements

type RubyElement struct {
    *Element
}

func RUBY() *RubyElement {
    return &RubyElement{
        Element: &Element{
            Tag: "ruby",
            IsSelfClosing: false,
        },
    }
}

func (e *RubyElement) AddChildren(children ...*Element) *RubyElement {
    e.Children = append(e.Children, children...)
	return e
}


