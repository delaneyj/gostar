package  elements

type MetaElement struct {
    *Element
}

func META() *MetaElement {
    return &MetaElement{
        Element: &Element{
            Tag: "meta",
            IsSelfClosing: true,
        },
    }
}

func (e *MetaElement) AddChildren(children ...*Element) *MetaElement {
    e.Children = append(e.Children, children...)
	return e
}


