package  elements

type BodyElement struct {
    *Element
}

func BODY() *BodyElement {
    return &BodyElement{
        Element: &Element{
            Tag: "body",
            IsSelfClosing: false,
        },
    }
}

func (e *BodyElement) AddChildren(children ...*Element) *BodyElement {
    e.Children = append(e.Children, children...)
	return e
}


