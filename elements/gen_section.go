package  elements

type SectionElement struct {
    *Element
}

func SECTION() *SectionElement {
    return &SectionElement{
        Element: &Element{
            Tag: "section",
            IsSelfClosing: false,
        },
    }
}

func (e *SectionElement) AddChildren(children ...*Element) *SectionElement {
    e.Children = append(e.Children, children...)
	return e
}


