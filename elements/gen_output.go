package  elements

type OutputElement struct {
    *Element
}

func OUTPUT() *OutputElement {
    return &OutputElement{
        Element: &Element{
            Tag: "output",
            IsSelfClosing: false,
        },
    }
}

func (e *OutputElement) AddChildren(children ...*Element) *OutputElement {
    e.Children = append(e.Children, children...)
	return e
}


