package  elements

type TableElement struct {
    *Element
}

func TABLE() *TableElement {
    return &TableElement{
        Element: &Element{
            Tag: "table",
            IsSelfClosing: false,
        },
    }
}

func (e *TableElement) AddChildren(children ...*Element) *TableElement {
    e.Children = append(e.Children, children...)
	return e
}


