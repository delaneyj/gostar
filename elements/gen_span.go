package  elements

type SpanElement struct {
    *Element
}

func SPAN() *SpanElement {
    return &SpanElement{
        Element: &Element{
            Tag: "span",
            IsSelfClosing: false,
        },
    }
}

func (e *SpanElement) AddChildren(children ...*Element) *SpanElement {
    e.Children = append(e.Children, children...)
	return e
}


