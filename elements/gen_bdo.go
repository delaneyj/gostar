type BdoElement struct {
    *Element
}

func BDO() *BdoElement {
    return &BdoElement{
        Element: &Element{
            Tag: "bdo",
            IsSelfClosing: false,
        },
    }
}

func (e *BdoElement) AddChildren(children ...*Element) *BdoElement {
    e.Children = append(e.Children, children...)
	return e
}


