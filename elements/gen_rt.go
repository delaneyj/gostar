type RtElement struct {
    *Element
}

func RT() *RtElement {
    return &RtElement{
        Element: &Element{
            Tag: "rt",
            IsSelfClosing: false,
        },
    }
}

func (e *RtElement) AddChildren(children ...*Element) *RtElement {
    e.Children = append(e.Children, children...)
	return e
}


