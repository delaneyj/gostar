package  elements

type CanvasElement struct {
    *Element
}

func CANVAS() *CanvasElement {
    return &CanvasElement{
        Element: &Element{
            Tag: "canvas",
            IsSelfClosing: false,
        },
    }
}

func (e *CanvasElement) AddChildren(children ...*Element) *CanvasElement {
    e.Children = append(e.Children, children...)
	return e
}


