package  elements

type SelectElement struct {
    *Element
}

func SELECT() *SelectElement {
    return &SelectElement{
        Element: &Element{
            Tag: "select",
            IsSelfClosing: false,
        },
    }
}

func (e *SelectElement) AddChildren(children ...*Element) *SelectElement {
    e.Children = append(e.Children, children...)
	return e
}


