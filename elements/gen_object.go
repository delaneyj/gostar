package  elements

type ObjectElement struct {
    *Element
}

func OBJECT() *ObjectElement {
    return &ObjectElement{
        Element: &Element{
            Tag: "object",
            IsSelfClosing: false,
        },
    }
}

func (e *ObjectElement) AddChildren(children ...*Element) *ObjectElement {
    e.Children = append(e.Children, children...)
	return e
}


