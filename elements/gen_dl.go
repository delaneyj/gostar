type DlElement struct {
    *Element
}

func DL() *DlElement {
    return &DlElement{
        Element: &Element{
            Tag: "dl",
            IsSelfClosing: false,
        },
    }
}

func (e *DlElement) AddChildren(children ...*Element) *DlElement {
    e.Children = append(e.Children, children...)
	return e
}


