package  elements

type SubElement struct {
    *Element
}

func SUB() *SubElement {
    return &SubElement{
        Element: &Element{
            Tag: "sub",
            IsSelfClosing: false,
        },
    }
}

func (e *SubElement) AddChildren(children ...*Element) *SubElement {
    e.Children = append(e.Children, children...)
	return e
}


