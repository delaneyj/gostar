package  elements

type ElementsElement struct {
    *Element
}

func ELEMENTS() *ElementsElement {
    return &ElementsElement{
        Element: &Element{
            Tag: "elements",
            IsSelfClosing: false,
        },
    }
}

func (e *ElementsElement) AddChildren(children ...*Element) *ElementsElement {
    e.Children = append(e.Children, children...)
	return e
}


