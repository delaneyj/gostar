package  elements

type ColElement struct {
    *Element
}

func COL() *ColElement {
    return &ColElement{
        Element: &Element{
            Tag: "col",
            IsSelfClosing: true,
        },
    }
}

func (e *ColElement) AddChildren(children ...*Element) *ColElement {
    e.Children = append(e.Children, children...)
	return e
}


