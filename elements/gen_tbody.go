package  elements

type TbodyElement struct {
    *Element
}

func TBODY() *TbodyElement {
    return &TbodyElement{
        Element: &Element{
            Tag: "tbody",
            IsSelfClosing: false,
        },
    }
}

func (e *TbodyElement) AddChildren(children ...*Element) *TbodyElement {
    e.Children = append(e.Children, children...)
	return e
}


