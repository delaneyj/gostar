package  elements

type ThElement struct {
    *Element
}

func TH() *ThElement {
    return &ThElement{
        Element: &Element{
            Tag: "th",
            IsSelfClosing: false,
        },
    }
}

func (e *ThElement) AddChildren(children ...*Element) *ThElement {
    e.Children = append(e.Children, children...)
	return e
}


