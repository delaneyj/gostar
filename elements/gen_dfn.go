package  elements

type DfnElement struct {
    *Element
}

func DFN() *DfnElement {
    return &DfnElement{
        Element: &Element{
            Tag: "dfn",
            IsSelfClosing: false,
        },
    }
}

func (e *DfnElement) AddChildren(children ...*Element) *DfnElement {
    e.Children = append(e.Children, children...)
	return e
}


