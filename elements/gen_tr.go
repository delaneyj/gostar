package  elements

type TrElement struct {
    *Element
}

func TR() *TrElement {
    return &TrElement{
        Element: &Element{
            Tag: "tr",
            IsSelfClosing: false,
        },
    }
}

func (e *TrElement) AddChildren(children ...*Element) *TrElement {
    e.Children = append(e.Children, children...)
	return e
}


