package  elements

type TfootElement struct {
    *Element
}

func TFOOT() *TfootElement {
    return &TfootElement{
        Element: &Element{
            Tag: "tfoot",
            IsSelfClosing: false,
        },
    }
}

func (e *TfootElement) AddChildren(children ...*Element) *TfootElement {
    e.Children = append(e.Children, children...)
	return e
}


