package  elements

type HgroupElement struct {
    *Element
}

func HGROUP() *HgroupElement {
    return &HgroupElement{
        Element: &Element{
            Tag: "hgroup",
            IsSelfClosing: false,
        },
    }
}

func (e *HgroupElement) AddChildren(children ...*Element) *HgroupElement {
    e.Children = append(e.Children, children...)
	return e
}


