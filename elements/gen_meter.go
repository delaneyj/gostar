package  elements

type MeterElement struct {
    *Element
}

func METER() *MeterElement {
    return &MeterElement{
        Element: &Element{
            Tag: "meter",
            IsSelfClosing: false,
        },
    }
}

func (e *MeterElement) AddChildren(children ...*Element) *MeterElement {
    e.Children = append(e.Children, children...)
	return e
}


