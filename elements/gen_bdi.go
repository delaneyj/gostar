package  elements

type BdiElement struct {
    *Element
}

func BDI() *BdiElement {
    return &BdiElement{
        Element: &Element{
            Tag: "bdi",
            IsSelfClosing: false,
        },
    }
}

func (e *BdiElement) AddChildren(children ...*Element) *BdiElement {
    e.Children = append(e.Children, children...)
	return e
}


