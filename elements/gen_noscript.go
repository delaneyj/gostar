package  elements

type NoscriptElement struct {
    *Element
}

func NOSCRIPT() *NoscriptElement {
    return &NoscriptElement{
        Element: &Element{
            Tag: "noscript",
            IsSelfClosing: true,
        },
    }
}

func (e *NoscriptElement) AddChildren(children ...*Element) *NoscriptElement {
    e.Children = append(e.Children, children...)
	return e
}


