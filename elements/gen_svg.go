package  elements

type SvgElement struct {
    *Element
}

func SVG() *SvgElement {
    return &SvgElement{
        Element: &Element{
            Tag: "svg",
            IsSelfClosing: false,
        },
    }
}

func (e *SvgElement) AddChildren(children ...*Element) *SvgElement {
    e.Children = append(e.Children, children...)
	return e
}


