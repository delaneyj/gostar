package  elements

type CiteElement struct {
    *Element
}

func CITE() *CiteElement {
    return &CiteElement{
        Element: &Element{
            Tag: "cite",
            IsSelfClosing: false,
        },
    }
}

func (e *CiteElement) AddChildren(children ...*Element) *CiteElement {
    e.Children = append(e.Children, children...)
	return e
}


