package  elements

type CaptionElement struct {
    *Element
}

func CAPTION() *CaptionElement {
    return &CaptionElement{
        Element: &Element{
            Tag: "caption",
            IsSelfClosing: false,
        },
    }
}

func (e *CaptionElement) AddChildren(children ...*Element) *CaptionElement {
    e.Children = append(e.Children, children...)
	return e
}


