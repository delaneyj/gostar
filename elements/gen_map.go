package  elements

type MapElement struct {
    *Element
}

func MAP() *MapElement {
    return &MapElement{
        Element: &Element{
            Tag: "map",
            IsSelfClosing: false,
        },
    }
}

func (e *MapElement) AddChildren(children ...*Element) *MapElement {
    e.Children = append(e.Children, children...)
	return e
}


