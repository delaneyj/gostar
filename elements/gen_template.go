package  elements

type TemplateElement struct {
    *Element
}

func TEMPLATE() *TemplateElement {
    return &TemplateElement{
        Element: &Element{
            Tag: "template",
            IsSelfClosing: true,
        },
    }
}

func (e *TemplateElement) AddChildren(children ...*Element) *TemplateElement {
    e.Children = append(e.Children, children...)
	return e
}


