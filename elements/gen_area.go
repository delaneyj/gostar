package  elements

type AreaElement struct {
    *Element
}

func AREA() *AreaElement {
    return &AreaElement{
        Element: &Element{
            Tag: "area",
            IsSelfClosing: true,
        },
    }
}

func (e *AreaElement) AddChildren(children ...*Element) *AreaElement {
    e.Children = append(e.Children, children...)
	return e
}


