package  elements

type SummaryElement struct {
    *Element
}

func SUMMARY() *SummaryElement {
    return &SummaryElement{
        Element: &Element{
            Tag: "summary",
            IsSelfClosing: false,
        },
    }
}

func (e *SummaryElement) AddChildren(children ...*Element) *SummaryElement {
    e.Children = append(e.Children, children...)
	return e
}


