package  elements

type LiElement struct {
    *Element
}

func LI() *LiElement {
    return &LiElement{
        Element: &Element{
            Tag: "li",
            IsSelfClosing: false,
        },
    }
}

func (e *LiElement) AddChildren(children ...*Element) *LiElement {
    e.Children = append(e.Children, children...)
	return e
}


