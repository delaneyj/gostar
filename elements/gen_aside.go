package  elements

type AsideElement struct {
    *Element
}

func ASIDE() *AsideElement {
    return &AsideElement{
        Element: &Element{
            Tag: "aside",
            IsSelfClosing: false,
        },
    }
}

func (e *AsideElement) AddChildren(children ...*Element) *AsideElement {
    e.Children = append(e.Children, children...)
	return e
}


