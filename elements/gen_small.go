package  elements

type SmallElement struct {
    *Element
}

func SMALL() *SmallElement {
    return &SmallElement{
        Element: &Element{
            Tag: "small",
            IsSelfClosing: false,
        },
    }
}

func (e *SmallElement) AddChildren(children ...*Element) *SmallElement {
    e.Children = append(e.Children, children...)
	return e
}


