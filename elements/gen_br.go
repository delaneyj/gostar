package  elements

type BrElement struct {
    *Element
}

func BR() *BrElement {
    return &BrElement{
        Element: &Element{
            Tag: "br",
            IsSelfClosing: true,
        },
    }
}

func (e *BrElement) AddChildren(children ...*Element) *BrElement {
    e.Children = append(e.Children, children...)
	return e
}


// Accesskey is the "accesskey" attribute.
// Keyboard shortcut to activate or focus element
// Valid values are constrained to the following:
//   * ordered_set_of_unique_space_separated_tokens
//   * identical_to
func (e *BrElement) Accesskey(v string) *BrElement {
    htmlStringer := NewSpaceDelimitedString()
    e.DelimitedStringAttributes["accesskey"] = htmlStringer
    htmlStringer.Add(v)
    return e
}

