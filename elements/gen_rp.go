type RpElement struct {
    *Element
}

func RP() *RpElement {
    return &RpElement{
        Element: &Element{
            Tag: "rp",
            IsSelfClosing: false,
        },
    }
}

func (e *RpElement) AddChildren(children ...*Element) *RpElement {
    e.Children = append(e.Children, children...)
	return e
}


