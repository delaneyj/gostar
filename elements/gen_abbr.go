package  elements

type AbbrElement struct {
    *Element
}

func ABBR() *AbbrElement {
    return &AbbrElement{
        Element: &Element{
            Tag: "abbr",
            IsSelfClosing: false,
        },
    }
}

func (e *AbbrElement) AddChildren(children ...*Element) *AbbrElement {
    e.Children = append(e.Children, children...)
	return e
}


