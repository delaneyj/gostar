package  elements

type HrElement struct {
    *Element
}

func HR() *HrElement {
    return &HrElement{
        Element: &Element{
            Tag: "hr",
            IsSelfClosing: true,
        },
    }
}

func (e *HrElement) AddChildren(children ...*Element) *HrElement {
    e.Children = append(e.Children, children...)
	return e
}


