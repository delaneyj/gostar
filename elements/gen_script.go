package  elements

type ScriptElement struct {
    *Element
}

func SCRIPT() *ScriptElement {
    return &ScriptElement{
        Element: &Element{
            Tag: "script",
            IsSelfClosing: true,
        },
    }
}

func (e *ScriptElement) AddChildren(children ...*Element) *ScriptElement {
    e.Children = append(e.Children, children...)
	return e
}


