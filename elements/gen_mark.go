package  elements

type MarkElement struct {
    *Element
}

func MARK() *MarkElement {
    return &MarkElement{
        Element: &Element{
            Tag: "mark",
            IsSelfClosing: false,
        },
    }
}

func (e *MarkElement) AddChildren(children ...*Element) *MarkElement {
    e.Children = append(e.Children, children...)
	return e
}


