package  elements

type SourceElement struct {
    *Element
}

func SOURCE() *SourceElement {
    return &SourceElement{
        Element: &Element{
            Tag: "source",
            IsSelfClosing: true,
        },
    }
}

func (e *SourceElement) AddChildren(children ...*Element) *SourceElement {
    e.Children = append(e.Children, children...)
	return e
}


