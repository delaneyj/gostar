type TrackElement struct {
    *Element
}

func TRACK() *TrackElement {
    return &TrackElement{
        Element: &Element{
            Tag: "track",
            IsSelfClosing: true,
        },
    }
}

func (e *TrackElement) AddChildren(children ...*Element) *TrackElement {
    e.Children = append(e.Children, children...)
	return e
}


