package  elements

type SearchElement struct {
    *Element
}

func SEARCH() *SearchElement {
    return &SearchElement{
        Element: &Element{
            Tag: "search",
            IsSelfClosing: false,
        },
    }
}

func (e *SearchElement) AddChildren(children ...*Element) *SearchElement {
    e.Children = append(e.Children, children...)
	return e
}


