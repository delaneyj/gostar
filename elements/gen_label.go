package  elements

type LabelElement struct {
    *Element
}

func LABEL() *LabelElement {
    return &LabelElement{
        Element: &Element{
            Tag: "label",
            IsSelfClosing: false,
        },
    }
}

func (e *LabelElement) AddChildren(children ...*Element) *LabelElement {
    e.Children = append(e.Children, children...)
	return e
}


