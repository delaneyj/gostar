package  elements

type ColgroupElement struct {
    *Element
}

func COLGROUP() *ColgroupElement {
    return &ColgroupElement{
        Element: &Element{
            Tag: "colgroup",
            IsSelfClosing: true,
        },
    }
}

func (e *ColgroupElement) AddChildren(children ...*Element) *ColgroupElement {
    e.Children = append(e.Children, children...)
	return e
}


