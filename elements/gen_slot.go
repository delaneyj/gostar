package  elements

type SlotElement struct {
    *Element
}

func SLOT() *SlotElement {
    return &SlotElement{
        Element: &Element{
            Tag: "slot",
            IsSelfClosing: false,
        },
    }
}

func (e *SlotElement) AddChildren(children ...*Element) *SlotElement {
    e.Children = append(e.Children, children...)
	return e
}


