type FigcaptionElement struct {
    *Element
}

func FIGCAPTION() *FigcaptionElement {
    return &FigcaptionElement{
        Element: &Element{
            Tag: "figcaption",
            IsSelfClosing: false,
        },
    }
}

func (e *FigcaptionElement) AddChildren(children ...*Element) *FigcaptionElement {
    e.Children = append(e.Children, children...)
	return e
}


