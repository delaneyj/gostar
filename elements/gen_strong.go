package  elements

type StrongElement struct {
    *Element
}

func STRONG() *StrongElement {
    return &StrongElement{
        Element: &Element{
            Tag: "strong",
            IsSelfClosing: false,
        },
    }
}

func (e *StrongElement) AddChildren(children ...*Element) *StrongElement {
    e.Children = append(e.Children, children...)
	return e
}


