type FigureElement struct {
    *Element
}

func FIGURE() *FigureElement {
    return &FigureElement{
        Element: &Element{
            Tag: "figure",
            IsSelfClosing: false,
        },
    }
}

func (e *FigureElement) AddChildren(children ...*Element) *FigureElement {
    e.Children = append(e.Children, children...)
	return e
}


