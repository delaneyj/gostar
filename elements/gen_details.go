package  elements

type DetailsElement struct {
    *Element
}

func DETAILS() *DetailsElement {
    return &DetailsElement{
        Element: &Element{
            Tag: "details",
            IsSelfClosing: false,
        },
    }
}

func (e *DetailsElement) AddChildren(children ...*Element) *DetailsElement {
    e.Children = append(e.Children, children...)
	return e
}


