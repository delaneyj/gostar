package  elements

type ArticleElement struct {
    *Element
}

func ARTICLE() *ArticleElement {
    return &ArticleElement{
        Element: &Element{
            Tag: "article",
            IsSelfClosing: false,
        },
    }
}

func (e *ArticleElement) AddChildren(children ...*Element) *ArticleElement {
    e.Children = append(e.Children, children...)
	return e
}


