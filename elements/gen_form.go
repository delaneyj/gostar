package  elements

type FormElement struct {
    *Element
}

func FORM() *FormElement {
    return &FormElement{
        Element: &Element{
            Tag: "form",
            IsSelfClosing: false,
        },
    }
}

func (e *FormElement) AddChildren(children ...*Element) *FormElement {
    e.Children = append(e.Children, children...)
	return e
}


