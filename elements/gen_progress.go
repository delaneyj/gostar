package  elements

type ProgressElement struct {
    *Element
}

func PROGRESS() *ProgressElement {
    return &ProgressElement{
        Element: &Element{
            Tag: "progress",
            IsSelfClosing: false,
        },
    }
}

func (e *ProgressElement) AddChildren(children ...*Element) *ProgressElement {
    e.Children = append(e.Children, children...)
	return e
}


