package  elements

type UlElement struct {
    *Element
}

func UL() *UlElement {
    return &UlElement{
        Element: &Element{
            Tag: "ul",
            IsSelfClosing: false,
        },
    }
}

func (e *UlElement) AddChildren(children ...*Element) *UlElement {
    e.Children = append(e.Children, children...)
	return e
}


