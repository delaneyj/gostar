package  elements

type CodeElement struct {
    *Element
}

func CODE() *CodeElement {
    return &CodeElement{
        Element: &Element{
            Tag: "code",
            IsSelfClosing: false,
        },
    }
}

func (e *CodeElement) AddChildren(children ...*Element) *CodeElement {
    e.Children = append(e.Children, children...)
	return e
}


