package  elements

type VarElement struct {
    *Element
}

func VAR() *VarElement {
    return &VarElement{
        Element: &Element{
            Tag: "var",
            IsSelfClosing: false,
        },
    }
}

func (e *VarElement) AddChildren(children ...*Element) *VarElement {
    e.Children = append(e.Children, children...)
	return e
}


