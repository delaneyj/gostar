package  elements

type DialogElement struct {
    *Element
}

func DIALOG() *DialogElement {
    return &DialogElement{
        Element: &Element{
            Tag: "dialog",
            IsSelfClosing: false,
        },
    }
}

func (e *DialogElement) AddChildren(children ...*Element) *DialogElement {
    e.Children = append(e.Children, children...)
	return e
}


