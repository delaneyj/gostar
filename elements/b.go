package  elements

import (
    "fmt"
	"github.com/delaneyj/gostar"
)

type BElement struct {
    *gostar.Element
}

func B(children ...fmt.Stringer) *BElement {
    return &BElement{
        Element: &gostar.Element{
            Tag: "b",
            IsSelfClosing: false,
            Children: children,
        },
    }
}

func (e *BElement) AddChildren(children ...fmt.Stringer) *BElement {
    e.Children = append(e.Children, children...)
    return e
}

func (e *BElement) TEXT(text string) *BElement {
    e.Children = append(e.Children, gostar.TEXT(text))
    return e
}

func (e *BElement) RAW(text string) *BElement {
    e.Children = append(e.Children, gostar.RAW(text))
    return e
}

func (e *BElement) CustomData(key, value string) *BElement {
    if e.CustomDataAttributes == nil {
        e.CustomDataAttributes = map[string]string{}
    }
	e.CustomDataAttributes[key] = value
	return e
}

func (e *BElement) CustomDataRemove(key string) *BElement {
	if e.CustomDataAttributes == nil {
		return e
	}
	delete(e.CustomDataAttributes, key)
	return e
}


