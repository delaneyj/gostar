package  elements

type HeaderElement struct {
    *Element
}

func HEADER() *HeaderElement {
    return &HeaderElement{
        Element: &Element{
            Tag: "header",
            IsSelfClosing: false,
        },
    }
}

func (e *HeaderElement) AddChildren(children ...*Element) *HeaderElement {
    e.Children = append(e.Children, children...)
	return e
}


